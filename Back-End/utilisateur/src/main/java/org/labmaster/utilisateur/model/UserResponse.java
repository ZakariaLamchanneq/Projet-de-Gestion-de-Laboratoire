package org.labmaster.utilisateur.model;

public class UserResponse {
    private String email;
    private String role;

    // Getters et setters


    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}